#!/usr/bin/env bash
# run_perf_validation.sh — per-case fault-tolerance perf gate.
#
# Implements `notes/perf-validation-protocol.md` (workspace repo,
# locked 2026-04-29 11:50 UTC). Runs the three tiers required for
# closure of any fault/<slug> branch:
#
#   reference  — cached, comes from results/_reference/<scale>/
#                (NOT re-run here; managed by refresh-reference.sh)
#   baseline   — this branch built clean, all fault hooks OFF
#   fault      — this branch, hook armed via SPTAG_FAULT_<SLUG_UPPER>=1
#
# Then writes closure.json with the four numeric comparisons and
# exits non-zero on any breach (>5% latency, <-5% qps, |Δrecall| > 0.005).
#
# Usage (on sdr):
#   evaluation/distributed/run_perf_validation.sh <slug> [--scale 1m|10m]
#                                                        [--workspace-repo PATH]
#                                                        [--cluster-conf PATH]
#                                                        [--worktree PATH]
#
# Defaults:
#   --scale            1m   (mandatory floor; 10m optional)
#   --workspace-repo   $HOME/workspace/openclaw-tasks
#                      (where tasks/distributed-index-scale-2k/results/ lives)
#   --cluster-conf     evaluation/distributed/configs/sdr_single.conf
#                      (single-node cluster pointing at localhost; if absent,
#                       generated on the fly from cluster.conf.example)
#   --worktree         $HOME/workspace/sptag-ft/wt/<slug>
#
# Hard guards:
#   * Refuses if hostname looks like spfresh-dev-amd-005.
#   * Refuses scale != {1m, 10m}.
#   * Refuses if reference metrics.json missing for the requested scale
#     (run refresh-reference.sh first).
#
# This script is the SINGLE owner of metrics.json+closure.json layout
# under tasks/distributed-index-scale-2k/results/<slug>/<SCALE>/. Per-case
# *_perf.sh wrappers are not used; the protocol mandates one runner.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
SPTAG_REPO="$(cd "${SCRIPT_DIR}/../.." && pwd)"

# ── Hard host guard ──
HOSTSHORT="$(hostname -s 2>/dev/null || hostname)"
if [[ "$HOSTSHORT" == *"spfresh-dev-amd-005"* || "$HOSTSHORT" == *"msccl-dev-000005"* ]]; then
    echo "REFUSING: never run perf-validation on spfresh-dev-amd-005" >&2
    exit 10
fi

SLUG=""
SCALE_LOW="1m"
WS_REPO="${HOME}/workspace/openclaw-tasks"
CLUSTER_CONF="${SCRIPT_DIR}/configs/sdr_single.conf"
WORKTREE=""

while [[ $# -gt 0 ]]; do
    case "$1" in
        --scale) SCALE_LOW="$2"; shift 2 ;;
        --workspace-repo) WS_REPO="$2"; shift 2 ;;
        --cluster-conf)   CLUSTER_CONF="$2"; shift 2 ;;
        --worktree)       WORKTREE="$2"; shift 2 ;;
        --) shift; break ;;
        -*) echo "unknown flag: $1" >&2; exit 2 ;;
        *)  if [[ -z "$SLUG" ]]; then SLUG="$1"; else echo "extra arg: $1" >&2; exit 2; fi; shift ;;
    esac
done

[[ -n "$SLUG" ]] || { echo "Usage: $0 <slug> [--scale 1m|10m]" >&2; exit 2; }
case "$SCALE_LOW" in 1m|10m) ;; *) echo "REFUSING: scale '$SCALE_LOW' (allowed: 1m, 10m)" >&2; exit 11 ;; esac
SCALE_HI="${SCALE_LOW^^}"
SLUG_UPPER="$(echo "$SLUG" | tr 'a-z-' 'A-Z_')"
WORKTREE="${WORKTREE:-$HOME/workspace/sptag-ft/wt/$SLUG}"

[[ -d "$WORKTREE" ]] || { echo "Worktree not found: $WORKTREE" >&2; exit 3; }
TEST_BIN="$WORKTREE/Release/SPTAGTest"
[[ -x "$TEST_BIN" ]] || { echo "SPTAGTest not built at $TEST_BIN; build first." >&2; exit 3; }

RESULTS_DIR="$WS_REPO/tasks/distributed-index-scale-2k/results"
CASE_DIR="$RESULTS_DIR/$SLUG/$SCALE_HI"
REF_DIR="$RESULTS_DIR/_reference/$SCALE_HI"
mkdir -p "$CASE_DIR/baseline" "$CASE_DIR/fault" "$CASE_DIR/reference"

REF_METRICS="$REF_DIR/metrics.json"
if [[ ! -f "$REF_METRICS" ]]; then
    echo "REFUSING: reference metrics missing: $REF_METRICS" >&2
    echo "  Run: $SCRIPT_DIR/refresh-reference.sh --scale $SCALE_LOW" >&2
    exit 4
fi
cp "$REF_METRICS" "$CASE_DIR/reference/metrics.json"
[[ -f "$REF_DIR/run.log" ]] && cp "$REF_DIR/run.log" "$CASE_DIR/reference/run.log"

# ── Auto-generate single-node cluster.conf if absent ──
if [[ ! -f "$CLUSTER_CONF" ]]; then
    HOST_IP="$(hostname -I | awk '{print $1}')"
    mkdir -p "$(dirname "$CLUSTER_CONF")"
    cat > "$CLUSTER_CONF" <<EOF
# Auto-generated by run_perf_validation.sh — single-node sdr profile.
[cluster]
ssh_user=$USER
sptag_dir=$WORKTREE
data_dir=${DATA_DIR:-/mnt_ssd/data}
tikv_version=v8.5.1
pd_version=v8.5.1
ssh_key=~/.ssh/id_ed25519

[nodes]
$HOST_IP 30002

[tikv]
$HOST_IP 2379 2380 20161
EOF
    echo "[perf-validation] generated $CLUSTER_CONF"
fi

run_one() {
    local tier="$1"     # baseline | fault
    local out_dir="$CASE_DIR/$tier"
    local out_log="$out_dir/run.log"
    local out_json="$out_dir/metrics.json"

    local hook_env=()
    if [[ "$tier" == "fault" ]]; then
        hook_env+=("SPTAG_FAULT_${SLUG_UPPER}=1")
    fi

    echo "[perf-validation:$SLUG] tier=$tier scale=$SCALE_LOW -> $out_json"
    pushd "$WORKTREE" >/dev/null

    # Drive the existing harness for the single-node flow. It writes
    # output_<scale>_1node.json next to the binary; we move it into place.
    env "${hook_env[@]}" \
        "$SCRIPT_DIR/run_distributed.sh" run "$CLUSTER_CONF" "$SCALE_LOW" 1 \
        2>&1 | tee "$out_log"
    local rc=${PIPESTATUS[0]}
    popd >/dev/null

    local emitted="$WORKTREE/output_${SCALE_LOW}_1node.json"
    if [[ -f "$emitted" ]]; then
        cp "$emitted" "$out_json"
    else
        echo "[perf-validation:$SLUG] expected output JSON not found at $emitted" >&2
        return 5
    fi
    return $rc
}

run_one baseline
run_one fault

# ── Closure summary (four numeric comparisons vs reference) ──
PY_CLOSURE='
import json, os, sys
case  = os.environ["SLUG"]
scale = os.environ["SCALE_HI"]
def load(p): return json.load(open(p))
ref = load(os.environ["REF_JSON"])
bas = load(os.environ["BAS_JSON"])
flt = load(os.environ["FLT_JSON"])

def steady(d):
    """Pluck steady-state query metrics from an SPFreshTest output JSON.
    Prefer the post-insert query block; fall back to the pre-insert one."""
    r = d.get("results", {})
    # Pre-insert query block (always emitted by RunBenchmark).
    pre = r.get("benchmark0b_query_before_insert_round2") \
          or r.get("benchmark0_query_before_insert") or {}
    # Post-insert insert/search blocks live under benchmarkN_insert.batch_K.
    last_batch = None
    for k in sorted(r):
        if k.endswith("_insert") and isinstance(r[k], dict):
            for bk in sorted(r[k]):
                if bk.startswith("batch_") and isinstance(r[k][bk], dict):
                    last_batch = r[k][bk]
    insert_qps = float(last_batch.get("insert throughput", 0)) if last_batch else 0
    search_block = (last_batch or {}).get("search_round2") \
                or (last_batch or {}).get("search") or pre
    def f(b, k, dflt=0): return float(b.get(k, dflt))
    recall = 0
    rb = search_block.get("recall")
    if isinstance(rb, dict): recall = float(rb.get("recallAtK", 0))
    return {
        "p50_ms":      f(search_block, "p50"),
        "p95_ms":      f(search_block, "p95"),
        "p99_ms":      f(search_block, "p99"),
        "query_qps":   f(search_block, "qps"),
        "insert_qps":  insert_qps,
        "recall_at_k": recall,
    }

R = steady(ref); B = steady(bas); F = steady(flt)

# Protocol thresholds: query latency p50/p95/p99 <= ref + 5%
# query_qps >= ref - 5%, insert_qps >= ref - 5%, |Δrecall| <= 0.005
def pct(now, base): return 0 if base == 0 else (now/base - 1) * 100
reasons = []
def chk(name, val, op, lim):
    if op == "<=" and val >  lim: reasons.append(f"{name}={val:.2f} > {lim:.2f}")
    if op == ">=" and val <  lim: reasons.append(f"{name}={val:.2f} < {lim:.2f}")
    if op == "abs" and abs(val) > lim: reasons.append(f"|Δ{name}|={abs(val):.4f} > {lim:.4f}")

# Compare BRANCH-BASELINE to REFERENCE (this is the perf-non-regression gate).
chk("p50_pct",      pct(B["p50_ms"],     R["p50_ms"]),     "<=", 5)
chk("p95_pct",      pct(B["p95_ms"],     R["p95_ms"]),     "<=", 5)
chk("p99_pct",      pct(B["p99_ms"],     R["p99_ms"]),     "<=", 5)
chk("query_qps_pct",  pct(B["query_qps"],  R["query_qps"]),  ">=", -5)
chk("insert_qps_pct", pct(B["insert_qps"], R["insert_qps"]), ">=", -5)
chk("recall_delta", B["recall_at_k"] - R["recall_at_k"],     "abs", 0.005)

# Sanity: fault tier ran and produced a recall not catastrophically lower
# than baseline (the fault might legitimately lower throughput briefly,
# but recall after recovery should be within 1pp of baseline).
recall_fault_delta = F["recall_at_k"] - B["recall_at_k"]
if abs(recall_fault_delta) > 0.01:
    reasons.append(f"|recall_fault-baseline|={abs(recall_fault_delta):.4f} > 0.01")

verdict = "PASS" if not reasons else "REGRESS"
out = {
    "case": case, "scale": scale,
    "run_id": os.environ["RUN_ID"], "timestamp_utc": os.environ["RUN_ID"],
    "host": os.uname().nodename,
    "reference": R, "baseline": B, "fault": F,
    "comparisons": {
        "baseline_vs_reference": {
            "p50_pct":         pct(B["p50_ms"],     R["p50_ms"]),
            "p95_pct":         pct(B["p95_ms"],     R["p95_ms"]),
            "p99_pct":         pct(B["p99_ms"],     R["p99_ms"]),
            "query_qps_pct":   pct(B["query_qps"],  R["query_qps"]),
            "insert_qps_pct":  pct(B["insert_qps"], R["insert_qps"]),
            "recall_delta":    B["recall_at_k"] - R["recall_at_k"],
        },
        "fault_vs_baseline": {
            "recall_delta":    F["recall_at_k"] - B["recall_at_k"],
        },
    },
    "thresholds": {
        "latency_pct_max": 5,
        "qps_pct_min": -5,
        "recall_abs_max": 0.005,
    },
    "verdict": verdict, "reasons": reasons,
}
json.dump(out, open(os.environ["OUT"], "w"), indent=2)
print(verdict + (": " + "; ".join(reasons) if reasons else ""))
sys.exit(0 if verdict == "PASS" else 6)
'

RUN_ID="$(date -u +%Y%m%dT%H%M%SZ)"
SLUG="$SLUG" SCALE_HI="$SCALE_HI" RUN_ID="$RUN_ID" \
REF_JSON="$CASE_DIR/reference/metrics.json" \
BAS_JSON="$CASE_DIR/baseline/metrics.json" \
FLT_JSON="$CASE_DIR/fault/metrics.json" \
OUT="$CASE_DIR/closure.json" \
python3 -c "$PY_CLOSURE" | tee "$CASE_DIR/verdict.txt"
RC=${PIPESTATUS[0]}

echo "[perf-validation:$SLUG] artifacts in $CASE_DIR"
exit $RC
